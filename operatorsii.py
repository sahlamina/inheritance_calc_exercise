import math


class Calctest:

    def addition(self, num1, num2):
        return num1 + num2

    def subtraction(self, num1, num2):
        return num1 - num2

    def division(self, num1, num2):
        return num1 / num2

    def multiply(self, num1, num2):
        return num1 * num2

